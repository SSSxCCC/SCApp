package com.sc.scapp;

public class Program {

    private int nameId;
    private int imageId;

    public Program(int nameId, int imageId) {
        this.nameId = nameId;
        this.imageId = imageId;
    }

    public int getNameId() {
        return nameId;
    }

    public int getImageId() {
        return imageId;
    }
}
